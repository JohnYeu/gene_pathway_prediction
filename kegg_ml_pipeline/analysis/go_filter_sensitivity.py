"""Supplementary GO term frequency filter sensitivity sweep.

Sweeps min_genes ∈ {5, 10, 15, 20} × max_fraction ∈ {0.20, 0.30, 0.50}
(12 cells), trains a fresh XGBoost with 5-fold CV at each cell, and writes:
    results/supp_go_filter_sweep/sweep.csv
    results/supp_go_filter_sweep/readme.txt

This script is independent of the main pipeline (it does NOT call
step6_train_xgboost.train_with_cv, which would overwrite the official
results/step6_cv_scores.png), and reuses the same 5-fold split across
all cells for comparability.

Usage (from repo root or anywhere):
    python kegg_ml_pipeline/analysis/go_filter_sensitivity.py
"""
from __future__ import annotations

import os
import sys
from pathlib import Path

# Lock cwd to the pipeline directory so relative paths in config.py
# (`data/...`, `results/...`) resolve correctly, and add the pipeline
# directory to sys.path so `import config` and `import step3_...` work.
PIPELINE_DIR = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(PIPELINE_DIR))
os.chdir(PIPELINE_DIR)

import numpy as np
import pandas as pd
import xgboost as xgb
from sklearn.metrics import roc_auc_score
from sklearn.model_selection import StratifiedKFold

import config
from step3_go_annotation import (
    filter_go_terms,
    get_all_go_terms,
    parse_go_annotation_source,
)
from step5_build_dataset import build_dataset
from utils.io_utils import load_pathways_tsv


OUT_DIR = "results/supp_go_filter_sweep"
MIN_GENES_GRID    = [5, 10, 15, 20]
MAX_FRACTION_GRID = [0.20, 0.30, 0.50]


def main() -> None:
    os.makedirs(OUT_DIR, exist_ok=True)

    raw_gene_go = parse_go_annotation_source(config.GO_ANNOTATION)
    all_pathways = load_pathways_tsv(config.ALL_PATHWAYS)

    records: list[dict] = []
    for min_genes in MIN_GENES_GRID:
        for max_frac in MAX_FRACTION_GRID:
            print(f"\n=== min_genes={min_genes}, max_fraction={max_frac} ===")
            gg, stats = filter_go_terms(raw_gene_go, min_genes, max_frac)
            vocab = get_all_go_terms(gg)
            X, y = build_dataset(all_pathways, gg, vocab)

            skf = StratifiedKFold(
                n_splits=5,
                shuffle=True,
                random_state=config.RANDOM_STATE,
            )
            aurocs: list[float] = []
            for fold, (train_i, val_i) in enumerate(skf.split(X, y), 1):
                model = xgb.XGBClassifier(**config.XGB_PARAMS)
                model.fit(X[train_i], y[train_i])
                proba = model.predict_proba(X[val_i])[:, 1]
                auc = roc_auc_score(y[val_i], proba)
                aurocs.append(float(auc))
                print(f"  fold {fold}: AUROC={auc:.4f}")

            records.append({
                "min_genes":     min_genes,
                "max_fraction":  max_frac,
                "n_terms":       stats["n_terms_after"],
                "n_genes":       stats["n_genes_after"],
                "feature_dim":   stats["n_terms_after"] + 6,
                "cv_auroc_mean": float(np.mean(aurocs)),
                "cv_auroc_std":  float(np.std(aurocs)),
            })

    df = pd.DataFrame(records)
    csv_path = os.path.join(OUT_DIR, "sweep.csv")
    df.to_csv(csv_path, index=False)
    print(f"\nSaved → {csv_path}")
    print(df.to_string(index=False))

    readme_path = os.path.join(OUT_DIR, "readme.txt")
    with open(readme_path, "w") as f:
        f.write(
            "Supplementary GO filter sensitivity sweep.\n"
            "Generated by: "
            "python kegg_ml_pipeline/analysis/go_filter_sensitivity.py\n"
            f"Grid: min_genes ∈ {MIN_GENES_GRID} × "
            f"max_fraction ∈ {MAX_FRACTION_GRID}\n"
            f"All {len(MIN_GENES_GRID) * len(MAX_FRACTION_GRID)} cells reuse "
            "the same 5-fold StratifiedKFold "
            f"(random_state={config.RANDOM_STATE}).\n"
            "Cells reuse XGB_PARAMS from config.py without retuning.\n"
        )
    print(f"Saved → {readme_path}")


if __name__ == "__main__":
    main()
